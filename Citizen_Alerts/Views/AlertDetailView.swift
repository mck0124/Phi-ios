struct PhotoThumbnailView: View {
    let photoName: String
    let index: Int
    let totalCount: Int
    @State private var showingFullScreen = false
    
    var body: some View {
        Button(action: { showingFullScreen = true }) {
            ZStack {
                // Placeholder image with gradient background
                RoundedRectangle(cornerRadius: 12)
                    .fill(
                        LinearGradient(
                            colors: [Color.blue.opacity(0.3), Color.purple.opacity(0.3)],
                            startPoint: .topLeading,
                            endPoint: .bottomTrailing
                        )
                    )
                    .frame(width: 120, height: 120)
                
                VStack(spacing: 8) {
                    Image(systemName: "photo.fill")
                        .font(.title2)
                        .foregroundColor(.white)
                    
                    Text("Photo \(index)")
                        .font(.caption)
                        .fontWeight(.medium)
                        .foregroundColor(.white)
                }
                
                // Photo count badge for multiple photos
                if totalCount > 1 {
                    VStack {
                        HStack {
                            Spacer()
                            Text("\(totalCount)")
                                .font(.caption2)
                                .fontWeight(.bold)
                                .foregroundColor(.white)
                                .padding(.horizontal, 6)
                                .padding(.vertical, 2)
                                .background(Color.black.opacity(0.6))
                                .cornerRadius(8)
                        }
                        Spacer()
                    }
                    .padding(8)
                }
            }
        }
        .sheet(isPresented: $showingFullScreen) {
            PhotoGalleryView(photoName: photoName, index: index, totalCount: totalCount)
        }
    }
}

struct PhotoGalleryView: View {
    let photoName: String
    let index: Int
    let totalCount: Int
    @Environment(\.dismiss) private var dismiss
    
    var body: some View {
        NavigationView {
            ZStack {
                // Full screen placeholder
                RoundedRectangle(cornerRadius: 0)
                    .fill(
                        LinearGradient(
                            colors: [Color.blue.opacity(0.4), Color.purple.opacity(0.4)],
                            startPoint: .topLeading,
                            endPoint: .bottomTrailing
                        )
                    )
                    .ignoresSafeArea()
                
                VStack(spacing: 20) {
                    Image(systemName: "photo.fill")
                        .font(.system(size: 80))
                        .foregroundColor(.white)
                    
                    VStack(spacing: 8) {
                        Text("Photo \(index) of \(totalCount)")
                            .font(.title2)
                            .fontWeight(.semibold)
                            .foregroundColor(.white)
                        
                        Text("Sample Photo: \(photoName)")
                            .font(.body)
                            .foregroundColor(.white.opacity(0.8))
                    }
                }
            }
            .navigationTitle("Photo Gallery")
            .navigationBarModifiers()
            .toolbar {
                ToolbarItem(placement: .navigationBarTrailing) {
                    Button("Done") {
                        dismiss()
                    }
                }
            }
        }
    }
}
